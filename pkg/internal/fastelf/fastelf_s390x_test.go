const libbsdS390xPath = "/usr/lib/s390x-linux-gnu/libbsd.so.0.12.1"

// TestFastElf_Data tests NewElfContextFromData with a native s390x ELF.
func TestFastElf_Data(t *testing.T) {
	data, err := os.ReadFile(libbsdS390xPath)
	if err != nil {
		t.Skipf("skipping: s390x libbsd not found at %s: %v", libbsdS390xPath, err)
	}
	ctx, err := NewElfContextFromData(data)
	require.NoError(t, err)
	testFastElf(t, ctx)
}

// TestFastElf_File tests NewElfContextFromFile with a native s390x ELF.
func TestFastElf_File(t *testing.T) {
	if _, err := os.Stat(libbsdS390xPath); err != nil {
		t.Skipf("skipping: s390x libbsd not found at %s: %v", libbsdS390xPath, err)
	}
	ctx, err := NewElfContextFromFile(libbsdS390xPath)
	require.NoError(t, err)
	testFastElf(t, ctx)
}

// TestFastElf_FileNoSections tests a native s390x minimal ELF with no sections.
func TestFastElf_FileNoSections(t *testing.T) {
	filePath, cleanup, err := writeTempFile(minimal_elf_s390x)
	require.NoError(t, err)
	defer cleanup()

	ctx, err := NewElfContextFromFile(filePath)
	require.NoError(t, err)

	require.Empty(t, ctx.Sections)
	require.Len(t, ctx.Segments, 4)
	require.False(t, ctx.HasSymbol("setprogname"))
	require.False(t, ctx.HasSection(".gnu_debuglink"))

	require.NoError(t, ctx.Close())
}
